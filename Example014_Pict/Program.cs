void PrintPic(int[,] pic)
{
    for (int i = 0; i < pic.GetLength(0); i++)
    {
        for (int j = 0; j < pic.GetLength(1); j++)
        {
            if(pic[i,j] == 0) Console.Write(" ");
            else 
                if(pic[i, j] == 2) Console.Write("*");
                else Console.Write("+");

        }
        Console.WriteLine();
    }
}

void FillPic(int row, int col)
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 2;
        FillPic(row - 1, col);
        FillPic(row + 1, col);
        FillPic(row, col - 1);
        FillPic(row, col + 1);
    }
}

